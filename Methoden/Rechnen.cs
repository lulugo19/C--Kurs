class Rechnen 
{
    // Hier werden Methoden mit verschiedenen 'Parametern' erstellt
    public static double Addieren(double a, double b) 
    {
        return a + b;
    }

    public static double Subtrahieren(double a, double b) 
    {
        return a - b;
    }

    public static double Multiplizieren(double a, double b) 
    {
        return a * b;
    }

    public static double Multiplizieren(double a, double b, double c) 
    {
        return a * b * c;
    }

    public static double Dividieren(double a, double b) 
    {
        return a / b;
    }

    public static double HochDrei(double x)
    {
        return x * x * x;
    }

    public static double Quadrat(double x)
    {
        return x * x;
    }
}